void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int key;

	i = 1;
	while (i < size)
	{
		j = i - 1;
		key = tab[i];
		while (j >= 0 && tab[j] > key)
		{
			tab[j + 1] = tab[j];
			j--;
		}
		tab[j + 1] = key;
		i++;
	}
}
