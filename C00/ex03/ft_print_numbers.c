#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char c1;

	c1 = '0';
	while (c1 <= '9')
	{
		ft_putchar(c1);
		c1++;
	}
}
